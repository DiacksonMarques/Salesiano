                     <p>A Pastoral é ação da comunidade eclesial que torna presente Jesus Cristo e a sua realidade de salvação como desenvolvimento pessoal e humano. Vive duas realidades: Jesus Cristo como referência e a realidade social e cultural do grupo humano envolvido. Não existe trabalho, projeto, presença, serviço salesiano sem a Pastoral Juvenil Salesiana, que enriquece a ação citada acima com o dom do carisma salesiano, herdado por Dom Bosco, nosso Pai, Mestre e fundador, cuja opção são os jovens de forma especial os mais pobres. A Pastoral Juvenil Salesiana é a forma de viver e atuar a missão salesiana segundo a Espiritualidade Juvenil Salesiana e o Sistema Preventivo de Dom Bosco. O Sistema Preventivo é um conjunto de práticas e princípios deixados por Dom Bosco e aplicado nos cinco continentes. Baseia-se em três pilares: razão, religião e amorevollezza.</p>
