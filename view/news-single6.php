                        dadas fizeram uma oração e abraçaram a Igreja, gesto simbólico de abraçar a
                        vida, os irmãos e todos que precisam de um olhar e um abraço na certeza de
                        que viver é bem melhor!</p>

                     <p>A manhã foi finalizada com a presença das psicólogas, Andrya Kelly,
                        Fázia Torres, Sandra Soraia e da enfermeira Soraya Lopes, que realizaram um
                        bate-papo com as turmas sobre a Valorização da Vida, Saúde Mental e Projeto
                        de Vida. Foi uma manhã rica em afeto, reflexão e conhecimento. A ação foi
                        coordenada pelo SOE (Serviço de Orientação Escolar) e pela Pastoral Juvenil
                        Salesiana. Foi um momento importante de valorização da vida e reflexão sobre
                        a temática, ressalta o Ir. Yuri Kiuski, coordenador de pastoral.</p>

